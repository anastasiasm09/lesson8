class Colors
  def colors(number)
    if number == 1
      'красный'

    elsif number == 2
      'оранжевый'

    elsif number == 3
      'желтый'

    elsif number == 4
      'зеленый'

    elsif number == 5
      'голубой'

    elsif number == 6
      'синий'

    elsif number == 7
      'фиолетовый'

    else
      nil

    end
  end
end
